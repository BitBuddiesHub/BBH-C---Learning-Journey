#include <iostream>
using namespace std;

int main() {
    // Print table headers with tabs
    cout << "Name\t\tAge\tCity" << endl;
    cout << "-----------------------------" << endl;

    // Print table rows with data, aligned using \t
    cout << "Alice\t\t30\tNew York" << endl;
    cout << "Bob\t\t25\tLos Angeles" << endl;
    cout << "Charlie\t\t28\tChicago" << endl;
    cout << "David\t\t35\tSan Francisco" << endl;

    return 0;
}
